function wordBlank(myNoun, myAdjective, myVerb, myAdverb) {
    var result = "";
    result = "the " + " " + myAdjective + " " + myNoun + " " + myVerb + " " + myAdverb;
    return result;
}
console.log(wordBlank("dog", "big", "ran", "slowly"));
console.log(wordBlank("cat", "black", "sleeps", "daily"));

